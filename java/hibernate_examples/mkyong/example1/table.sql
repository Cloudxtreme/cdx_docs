CREATE TABLE DBUSER ( 
  USER_ID       NUMBER (5)    NOT NULL, 
  USERNAME      VARCHAR2 (20)  NOT NULL, 
  CREATED_BY    VARCHAR2 (20)  NOT NULL, 
  CREATED_DATE  DATE          NOT NULL, 
  PRIMARY KEY ( USER_ID ) 
);


CREATE SEQUENCE DBUSER_SEQ  
  START WITH 3  
  MAXVALUE 999999999999999999999999999  
  MINVALUE 1  
  NOCYCLE  
  CACHE 20  
  NOORDER;  



